import java.lang.Math;
import java.util.Scanner;

public class Udregninger {
    static int sum(int a, int b) {
        return a + b;
    }

    static int minus(int a, int b) {
        return a - b;
    }

    static int dele(int a, int b) {
        return a / b;
    }

    static int multiplicere(int a, int b) {
        return a * b;
    }

    static int secret(int a, int b) {
        return (a * b * 4) / a;
    }
}

